#!/usr/bin/env python3
"""
A simple script that prints "Hello World" to the console.

This script follows Python best practices and is meant to be
executable on standard Python installations.
"""

def main():
    """Main function that prints 'Hello World' to the console."""
    print("Hello World")

if __name__ == "__main__":
    # This ensures the main function is only called when this script
    # is executed directly (not when imported as a module)
    main()